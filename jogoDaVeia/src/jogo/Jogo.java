    public void vezDoJogador(){
        if (jogadorX && jogadorO){
            this.op = 0;
            jogadorX = false;
            jogadorO = true;
            visor.setText("Vez do Jogador O");
        } else if (jogadorX && jogadorO == false){
            this.op = 0;
            jogadorX = false;
            jogadorO = true;
            visor.setText("Vez do Jogador O");
        } else {
            this.op = 1;
            jogadorX = true;
            jogadorO = false;
            visor.setText("Vez do Jogador X");
        }
    }
    
