    public void resetarJogo(){
        for(i = 0; i < 3; i++){
            for(j = 0; j < 3; j++){
                posicaoVet[i][j] = "";
            }
        }
    }
    
    public void verificarVencedor(){
        linha1 = posicaoVet[0][0] + posicaoVet[0][1] + posicaoVet[0][2];
        linha2 = posicaoVet[1][0] + posicaoVet[1][1] + posicaoVet[1][2];
        linha3 = posicaoVet[2][0] + posicaoVet[2][1] + posicaoVet[2][2];
        coluna1 = posicaoVet[0][0] + posicaoVet[1][0] + posicaoVet[2][0];
        coluna2 = posicaoVet[0][1] + posicaoVet[1][1] + posicaoVet[1][2];
        coluna3 = posicaoVet[2][0] + posicaoVet[2][1] + posicaoVet[2][2];
        diagonal1 = posicaoVet[0][0] + posicaoVet[1][1] + posicaoVet[2][2];
        diagonal2 = posicaoVet[0][2] + posicaoVet[1][1] + posicaoVet[2][0];
        
        if (linha1.equals("xxx") || linha2.equals("xxx") || linha3.equals("xxx") ||
            coluna1.equals("xxx") || coluna2.equals("xxx") || coluna3.equals("xxx") ||
            diagonal1.equals("xxx") || diagonal2.equals("xxx")){
                visor.setText("O vencedor é o X");
                desativarBotoes();
        } else if (linha1.equals("ooo") || linha2.equals("ooo") || linha3.equals("ooo") ||
            coluna1.equals("ooo") || coluna2.equals("ooo") || coluna3.equals("ooo") ||
            diagonal1.equals("ooo") || diagonal2.equals("ooo")){
                visor.setText("O vencedor é o O");
                desativarBotoes();
        } else if (contadorPosicao == 9) {
            visor.setText("Nao ha vencedor");
        }
    }
    
    public void verificarPosicao(){
        contadorPosicao = 0;
        for(i = 0; i < 3; i++){
            for(j = 0; j < 3; j++){
                if (posicaoVet[i][j] == "x" || posicaoVet[i][j] == "o"){
                    contadorPosicao+=1;
                }
            }
        }
    }
    
